// Filter countries that have calling codes (supported by libphonenumber-js)
const COUNTRIES = ALL_COUNTRIES.filter(country => {
  try {
    getCountryCallingCode(country.code as LibCountryCode);
    return true;
  } catch {
    return false;
  }
});

type CountryCode = string;

interface PhoneInputProps {
  value?: string;
  onChange?: (value: string | undefined) => void;
  placeholder?: string;
  className?: string;
  disabled?: boolean;
  required?: boolean;
  'aria-label'?: string;
  'aria-describedby'?: string;
}

export function PhoneInputComponent({
  value,
  onChange,
  placeholder = "Phone number",
  className,
  disabled = false,
  required = false,
  ...props
}: PhoneInputProps) {
  const [selectedCountry, setSelectedCountry] = useState<CountryCode>('US');
  const [phoneNumber, setPhoneNumber] = useState('');
  const [open, setOpen] = useState(false);
  const isUpdatingRef = useRef(false);

  // Parse initial value
  useEffect(() => {
    if (isUpdatingRef.current) {
      isUpdatingRef.current = false;
      return;
    }
    
    if (value) {
      try {
        const parsed = parsePhoneNumber(value);
        if (parsed && parsed.country) {
          setSelectedCountry(parsed.country as CountryCode);
          setPhoneNumber(parsed.nationalNumber);
        }
      } catch {
        // If parsing fails, try to extract country code and number
        if (value.startsWith('+')) {
          const match = value.match(/^\+(\d{1,4})(.*)$/);
          if (match) {
            const [, code, number] = match;
            // Try to find country by calling code
            const country = COUNTRIES.find(c => {
              try {
                return getCountryCallingCode(c.code as LibCountryCode) === code;
              } catch {
                return false;
              }
            });
            if (country) {
              setSelectedCountry(country.code);
              setPhoneNumber(number);
            } else {
              setPhoneNumber(value);
            }
          } else {
            setPhoneNumber(value);
          }
        } else {
          setPhoneNumber(value);
        }
      }
    } else {
      setPhoneNumber('');
    }
  }, [value]);

  const handleCountryChange = (countryCode: CountryCode) => {
    setSelectedCountry(countryCode);
    setOpen(false);
    if (phoneNumber) {
      try {
        const callingCode = getCountryCallingCode(countryCode as LibCountryCode);
        const newValue = `+${callingCode}${phoneNumber}`;
        isUpdatingRef.current = true;
        onChange?.(newValue);
      } catch (error) {
        console.error('Error getting calling code:', error);
        // If we can't get the calling code, just pass the raw number
        isUpdatingRef.current = true;
        onChange?.(phoneNumber);
      }
    }
  };

  const handlePhoneNumberChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    const inputValue = e.target.value.replace(/\D/g, ''); // Remove non-digits
    setPhoneNumber(inputValue);
    
    if (inputValue) {
      try {
        const callingCode = getCountryCallingCode(selectedCountry as LibCountryCode);
        const newValue = `+${callingCode}${inputValue}`;
        isUpdatingRef.current = true;
        onChange?.(newValue);
      } catch (error) {
        console.error('Error getting calling code:', error);
        // If we can't get the calling code, just pass the raw number
        isUpdatingRef.current = true;
        onChange?.(inputValue);
      }
    } else {
      isUpdatingRef.current = true;
      onChange?.(undefined);
    }
  };

  const selectedCountryData = COUNTRIES.find(c => c.code === selectedCountry);
  const callingCode = selectedCountryData ? (() => {
    try {
      return getCountryCallingCode(selectedCountry as LibCountryCode);
    } catch {
      return '';
    }
  })() : '';

  return (
    <div className={cn("flex", className)}>
      <Popover open={open} onOpenChange={setOpen}>
        <PopoverTrigger asChild>
          <Button
            variant="outline"
            role="combobox"
            aria-expanded={open}
            className="w-[140px] justify-between rounded-r-none border-r-0 focus:z-10"
            disabled={disabled}
          >
            <div className="flex items-center gap-2">
              <span className="text-lg">{getCountryFlag(selectedCountry)}</span>
              <span className="text-sm text-muted-foreground">+{callingCode}</span>
            </div>
            <ChevronDown className="ml-2 h-4 w-4 shrink-0 opacity-50" />
          </Button>
        </PopoverTrigger>
        <PopoverContent className="w-[300px] p-0">
          <Command>
            <CommandInput placeholder="Search countries..." />
            <CommandList>
              <CommandEmpty>No country found.</CommandEmpty>
              <CommandGroup>
                {COUNTRIES.map((country) => {
                  const flag = getCountryFlag(country.code);
                  let callingCode = '';
                  try {
                    callingCode = getCountryCallingCode(country.code as LibCountryCode);
                  } catch {
                    // Skip countries without calling codes
                    return null;
                  }
                  
                  return (
                    <CommandItem
                      key={country.code}
                      value={`${country.name} ${country.code} ${callingCode}`}
                      onSelect={() => handleCountryChange(country.code)}
                    >
                      <div className="flex items-center gap-2 flex-1">
                        <span className="text-lg">{flag}</span>
                        <span className="text-sm">{country.name}</span>
                        <span className="text-xs text-muted-foreground ml-auto">
                          +{callingCode}
                        </span>
                      </div>
                      <Check
                        className={cn(
                          "ml-auto h-4 w-4",
                          selectedCountry === country.code ? "opacity-100" : "opacity-0"
                        )}
                      />
                    </CommandItem>
                  );
                })}
              </CommandGroup>
            </CommandList>
          </Command>
        </PopoverContent>
      </Popover>
      
      <Input
        type="tel"
        value={phoneNumber}
        onChange={handlePhoneNumberChange}
        placeholder={placeholder || "Phone number"}
        className="rounded-l-none focus:z-10"
        disabled={disabled}
        required={required}
        {...props}
      />
    </div>
  );
}

export { PhoneInputComponent as PhoneInput };
